public class Country {
    private String countryName;
    private String countryCapital;
    private String countryRegion;
    private String countryPopulation;
    private String countryDomain;
    private String countrySubRegion;
    private String countryLatLang;
    private String countryTimeZone;

    public String getCountryDomain() {
        return countryDomain;
    }

    public void setCountryDomain(String countryDomain) {
        this.countryDomain = countryDomain;
    }

    public String getCountrySubRegion() {
        return countrySubRegion;
    }

    public void setCountrySubRegion(String countrySubRegion) {
        this.countrySubRegion = countrySubRegion;
    }

    public String getCountryLatLang() {
        return countryLatLang;
    }

    public void setCountryLatLang(String countryLatLang) {
        this.countryLatLang = countryLatLang;
    }

    public String getCountryTimeZone() {
        return countryTimeZone;
    }

    public void setCountryTimeZone(String countryTimeZone) {
        this.countryTimeZone = countryTimeZone;
    }

    public String getCountryName() {
        return countryName;
    }

    public void setCountryName(String countryName) {
        this.countryName = countryName;
    }

    public String getCountryCapital() {
        return countryCapital;
    }

    public void setCountryCapital(String countryCapital) {
        this.countryCapital = countryCapital;
    }

    public String getCountryRegion() {
        return countryRegion;
    }

    public void setCountryRegion(String countryRegion) {
        this.countryRegion = countryRegion;
    }

    public String getCountryPopulation() {
        return countryPopulation;
    }

    public void setCountryPopulation(String countryPopulation) {
        this.countryPopulation = countryPopulation;
    }
}
